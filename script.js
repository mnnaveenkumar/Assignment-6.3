var temperatureInCelsius = 40;

// calculate Celsious
var celsiusToFahrenheit;

celsiusToFahrenheit = (temperatureInCelsius * 9) / 5 + 32;

console.log("Temperature of " + temperatureInCelsius + " celsius, will be " + celsiusToFahrenheit + " fahrenheit");


